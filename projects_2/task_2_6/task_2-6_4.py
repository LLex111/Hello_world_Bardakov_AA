way = input("Прямо, Направо, Налево: ").lower()
if way == "прямо":
    print("Как пряму ехати — живу не бывати — нет пути ни прохожему, ни проезжему, ни пролетному")
elif way == "направо":
    print("Направу ехати — женату быти")
elif way == "налево":
    print("Налеву ехати — богату быти")
else:
    print("Нет такого пути!!!")